// classFieldColumns parses the brace-balanced body of the class whose head ends
// at headEnd and emits a SCOPE.Schema/column per Dart field declaration.
func classFieldColumns(src string, headEnd int, model, framework, provenance, path string, modelLine int) []types.EntityRecord {
	open := headEnd + indexBrace(src[headEnd:])
	if open < headEnd || open >= len(src) {
		return nil
	}
	end := findBraceEndByte(src, open)
	if open+1 > end || end > len(src) {
		return nil
	}
	body := src[open+1 : end]
	var out []types.EntityRecord
	seen := map[string]bool{}
	for _, f := range dartFieldRe.FindAllStringSubmatchIndex(body, -1) {
		colType := strings.TrimSuffix(body[f[2]:f[3]], "?")
		colName := body[f[4]:f[5]]
		if seen[colName] || isDartFieldKeyword(colType) {
			continue
		}
		seen[colName] = true
		cline := modelLine + strings.Count(body[:f[0]], "\n")
		col := newDartSchema(colName, "column", framework, path, cline, provenance)
		col.ID = col.ComputeID()
		col.Properties["column_type"] = colType
		col.Properties["model"] = model
		out = append(out, col)
	}
	return out
}

// isDartFieldKeyword rejects type tokens that are actually keywords (so a
// `return x;` or `final` mis-parse never becomes a column).
func isDartFieldKeyword(t string) bool {
	switch t {
	case "return", "final", "const", "late", "var", "void", "if", "for",
		"while", "static", "get", "set":
		return true
	}
	return false
}

// indexBrace returns the byte offset of the first `{` in s (or 0 if none, so
// findBraceEndByte degrades gracefully).
func indexBrace(s string) int {
	if i := strings.IndexByte(s, '{'); i >= 0 {
		return i
	}
	return 0
}

// splitList splits a `[A, B, C]`-style inner list into trimmed identifiers.
func splitList(s string) []string {
	var out []string
	for _, p := range strings.Split(s, ",") {
		t := strings.TrimSpace(p)
		if t != "" {
			out = append(out, t)
		}
	}
	return out
}

func minInt(a, b int) int {
	if a < b {
		return a
	}
	return b
}
