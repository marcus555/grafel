// persistence.go — Dart persistence / ORM model → table/column/collection
// schema synthesis (#5361, low-ranked-language epic #5360).
//
// Dart's mobile/Flutter ecosystem ships several embedded-database libraries.
// Before this extractor, zero Dart ORMs were tracked. This pass adds the four
// canonical persistence libraries (plus an honest-partial for raw sqflite SQL),
// mirroring the Crystal ORM shape (internal/custom/crystal/avram_orm.go) —
// SCOPE.Schema entities carrying framework + provenance props:
//
//   - drift  — annotation/class ORM. `class Foos extends Table { IntColumn get
//     id => integer()...; }` → table + typed columns. `@DriftDatabase(tables:
//     [Foos])` → a database SCOPE.Schema node.
//   - floor  — Room-inspired annotation ORM. `@entity class User { ... }` with
//     `@PrimaryKey` / `@ColumnInfo` fields → model + columns; `@dao` /
//     `@Database` carried as schema nodes.
//   - isar   — NoSQL embedded. `@collection class User { ... }` → a collection
//     model; `Id id;` and `@Index()`-decorated fields → columns.
//   - hive   — key/value typed boxes. `@HiveType(typeId: n) class User { ... }`
//     with `@HiveField(k)` → model + ordered fields.
//   - sqflite (honest-partial) — raw-SQL driver. `db.execute('CREATE TABLE
//     foos (id INTEGER PRIMARY KEY, name TEXT)')` → table + columns parsed from
//     the DDL. Query attribution / migrations are deferred.
//   - objectbox — `@Entity() class User { @Id() int id; }` → model + columns.
//
// Codegen awareness (#5361 item 4): freezed (`@freezed`) and json_serializable
// (`@JsonSerializable()`) annotations plus `part 'x.g.dart';` / `part
// 'x.freezed.dart';` directives are stamped onto the host model entity
// (codegen=freezed|json_serializable, has_part_g=true) so the graph records
// that the persisted/serialised shape is generated by build_runner.
//
// Honest exclusions / follow-ups (no fabricated schema):
//   - Query DSL attribution (drift `select(table)`, isar `.filter()`), DAO
//     method-level query parsing, migrations, and transactions are deferred.
//   - sqflite is raw-SQL only: non-`CREATE TABLE` statements are not modelled.
//
// Registration key: "custom_dart_persistence" (auto-dispatched for dart files
// via the `custom_dart_` prefix in custom_dispatch.go).
package dart

import (
	"context"
	"regexp"
	"strings"

	"github.com/cajasmota/grafel/internal/extractor"
	"github.com/cajasmota/grafel/internal/types"
)

func init() {
	extractor.Register("custom_dart_persistence", &persistenceExtractor{})
}

type persistenceExtractor struct{}

func (e *persistenceExtractor) Language() string { return "custom_dart_persistence" }

// ---------------------------------------------------------------------------
// Regexes
// ---------------------------------------------------------------------------

var (
	// drift — `class Foos extends Table {`. Group 1 = class (table) name.
	driftTableRe = regexp.MustCompile(
		`(?m)^[ \t]*(?:abstract\s+)?class\s+([A-Z]\w*)\s+extends\s+Table\b`)

	// drift typed column getter:
	//   `IntColumn get id => integer().autoIncrement()();`
	//   `TextColumn get name => text().withLength(min: 1)();`
	// Group 1 = Dart column type (IntColumn/TextColumn/...), 2 = field name,
	// 3 = the builder root (integer/text/boolean/real/dateTime/blob).
	driftColumnRe = regexp.MustCompile(
		`(?m)^[ \t]*([A-Za-z]+Column(?:<[^>]*>)?)\s+get\s+(\w+)\s*=>\s*([A-Za-z]+)\s*\(`)

	// drift database: `@DriftDatabase(tables: [Foos, Bars])`. Group 1 = the
	// raw table list. The annotated class name follows on a later line.
	driftDatabaseRe = regexp.MustCompile(
		`@DriftDatabase\s*\(\s*tables:\s*\[([^\]]*)\]`)

	// floor — `@entity` / `@Entity` preceding `class User {`. The bare /
	// no-parens form is floor's idiom; the parenthesised `@Entity()` form is
	// objectbox's (handled by objectboxEntityRe), so a `(` immediately after
	// the annotation is rejected here via the trailing `(?:\b|\s)` + negative
	// look — Go's RE2 has no lookahead, so we forbid a following `(` by
	// requiring the next char (if any on the line) not be `(`.
	floorEntityRe = regexp.MustCompile(
		`(?m)@[eE]ntity(?:\s|$)[^\n]*\n(?:[ \t]*@?\w[^\n]*\n)*?[ \t]*class\s+([A-Z]\w*)`)

	// floor — `@dao` / `@Dao` abstract class, and `@Database(...)` class.
	floorDaoRe = regexp.MustCompile(
		`(?m)@[dD]ao\b[^\n]*\n(?:[ \t]*@?\w[^\n]*\n)*?[ \t]*abstract\s+class\s+([A-Z]\w*)`)
	floorDatabaseRe = regexp.MustCompile(
		`(?m)@Database\s*\([^)]*\)[^\n]*\n(?:[ \t]*@?\w[^\n]*\n)*?[ \t]*abstract\s+class\s+([A-Z]\w*)`)

	// isar — `@collection` / `@Collection()` preceding `class User {`.
	isarCollectionRe = regexp.MustCompile(
		`(?m)@[cC]ollection(?:\(\))?\b[^\n]*\n(?:[ \t]*@?\w[^\n]*\n)*?[ \t]*class\s+([A-Z]\w*)`)

	// hive — `@HiveType(typeId: 0)` preceding `class User {`. Group 1 = the
	// typeId arg run, group 2 supplied by a follow-up class match.
	hiveTypeRe = regexp.MustCompile(
		`(?m)@HiveType\s*\(([^)]*)\)[^\n]*\n(?:[ \t]*@?\w[^\n]*\n)*?[ \t]*class\s+([A-Z]\w*)`)

	// objectbox — `@Entity()` (parenthesised, distinguishes from floor's bare
	// `@entity`) preceding `class User {`.
	objectboxEntityRe = regexp.MustCompile(
		`(?m)@Entity\s*\(\s*\)[^\n]*\n(?:[ \t]*@?\w[^\n]*\n)*?[ \t]*class\s+([A-Z]\w*)`)

	// Generic Dart field inside a model body: `final String name;`,
	// `int? age;`, `late String email;`, `String name = '';`. Group 1 = type,
	// group 2 = field name. Static / getters excluded by anchoring.
	dartFieldRe = regexp.MustCompile(
		`(?m)^[ \t]*(?:@\w+(?:\([^)]*\))?\s+)*(?:final\s+|late\s+|const\s+)*([A-Za-z_][\w<>]*\??)\s+(\w+)\s*(?:=|;)`)

	// hive field annotation marker on the immediately-following field:
	// `@HiveField(0) final String name;`. Group 1 = index.
	hiveFieldRe = regexp.MustCompile(
		`(?m)@HiveField\s*\(\s*(\d+)`)

	// sqflite raw DDL: `CREATE TABLE [IF NOT EXISTS] foos ( ... )`. Group 1 =
	// table name, group 2 = the column body.
	sqfliteCreateRe = regexp.MustCompile(
		`(?is)CREATE\s+TABLE\s+(?:IF\s+NOT\s+EXISTS\s+)?["'` + "`" + `]?(\w+)["'` + "`" + `]?\s*\(([^;]*?)\)\s*['"` + "`" + `]?\s*\)?\s*;`)

	// codegen markers.
	freezedRe    = regexp.MustCompile(`@[fF]reezed\b`)
	jsonSerialRe = regexp.MustCompile(`@JsonSerializable\s*\(`)
	partGRe      = regexp.MustCompile(`(?m)^[ \t]*part\s+'[^']*\.(g|freezed)\.dart'`)
)

// ---------------------------------------------------------------------------
// Pre-filter signals
// ---------------------------------------------------------------------------

// hasPersistenceSignal is a fast pre-filter: the file must reference at least
// one supported persistence library so we never misfire on plain classes.
func hasPersistenceSignal(content string) bool {
	for _, s := range []string{
		"extends Table", "DriftDatabase", "package:drift",
		"@entity", "@Entity", "package:floor",
		"@collection", "@Collection", "package:isar",
		"@HiveType", "package:hive",
		"package:objectbox",
		"CREATE TABLE", "package:sqflite", "openDatabase(",
	} {
		if strings.Contains(content, s) {
			return true
		}
	}
	return false
}

// ---------------------------------------------------------------------------
// Extract
// ---------------------------------------------------------------------------

func (e *persistenceExtractor) Extract(
	ctx context.Context,
	file extractor.FileInput,
) ([]types.EntityRecord, error) {
	if len(file.Content) == 0 || file.Language != "dart" {
		return nil, nil
	}
	src := string(file.Content)
	if !hasPersistenceSignal(src) {
		return nil, nil
	}

	codegen := detectCodegen(src)

	var out []types.EntityRecord
	out = append(out, extractDrift(src, file.Path, codegen)...)
	out = append(out, extractFloor(src, file.Path, codegen)...)
	out = append(out, extractIsar(src, file.Path, codegen)...)
	out = append(out, extractHive(src, file.Path, codegen)...)
	out = append(out, extractObjectbox(src, file.Path, codegen)...)
	out = append(out, extractSqflite(src, file.Path)...)
	return out, nil
}

// codegenInfo records the codegen markers found in a file.
type codegenInfo struct {
	freezed    bool
	jsonSerial bool
	hasPartG   bool
}

func detectCodegen(src string) codegenInfo {
	return codegenInfo{
		freezed:    freezedRe.MatchString(src),
		jsonSerial: jsonSerialRe.MatchString(src),
		hasPartG:   partGRe.MatchString(src),
	}
}

// stampCodegen attaches codegen-source provenance to a model entity.
func stampCodegen(e *types.EntityRecord, cg codegenInfo) {
	var parts []string
	if cg.freezed {
		parts = append(parts, "freezed")
	}
	if cg.jsonSerial {
		parts = append(parts, "json_serializable")
	}
	if len(parts) > 0 {
		e.Properties["codegen"] = strings.Join(parts, ",")
		e.Properties["build_runner"] = "true"
	}
	if cg.hasPartG {
		e.Properties["has_part_g"] = "true"
	}
}

