// ---------------------------------------------------------------------------
// hive
// ---------------------------------------------------------------------------

func extractHive(src, path string, cg codegenInfo) []types.EntityRecord {
	var out []types.EntityRecord
	for _, m := range hiveTypeRe.FindAllStringSubmatchIndex(src, -1) {
		typeIDArg := strings.TrimSpace(src[m[2]:m[3]])
		name := src[m[4]:m[5]]
		line := lineAt(src, m[0])
		model := newDartSchema(name, "model", "hive", path, line,
			"INFERRED_FROM_HIVE_TYPE")
		model.Properties["orm"] = "hive"
		model.Properties["store_kind"] = "key_value"
		if typeIDArg != "" {
			model.Properties["type_id"] = strings.TrimSpace(strings.TrimPrefix(typeIDArg, "typeId:"))
		}
		stampCodegen(&model, cg)
		model.ID = model.ComputeID()
		out = append(out, model)

		// hive fields carry a @HiveField(n) ordinal — parse the class body.
		bodyStart := m[5]
		bodyEnd := findBraceEndByte(src, bodyStart+indexBrace(src[bodyStart:]))
		if bodyStart < bodyEnd && bodyEnd <= len(src) {
			body := src[bodyStart:bodyEnd]
			fieldIdx := hiveFieldRe.FindAllStringSubmatchIndex(body, -1)
			fields := dartFieldRe.FindAllStringSubmatchIndex(body, -1)
			for _, f := range fields {
				colName := body[f[4]:f[5]]
				colType := strings.TrimSuffix(body[f[2]:f[3]], "?")
				cline := line + strings.Count(body[:f[0]], "\n")
				col := newDartSchema(colName, "field", "hive", path, cline,
					"INFERRED_FROM_HIVE_FIELD")
				col.Properties["column_type"] = colType
				col.Properties["model"] = name
				// Match the nearest preceding @HiveField ordinal. Anchor on the
				// field NAME offset (f[4]) — dartFieldRe consumes any leading
				// `@HiveField(n)` annotation so f[0] sits AT that annotation,
				// which would make every field bind to the wrong (earlier)
				// ordinal.
				if ord := nearestHiveOrdinal(body, fieldIdx, f[4]); ord != "" {
					col.Properties["hive_field"] = ord
				}
				col.ID = col.ComputeID()
				out = append(out, col)
			}
		}
	}
	return out
}

// nearestHiveOrdinal returns the @HiveField index whose match immediately
// precedes the field at fieldOff, or "" when none.
func nearestHiveOrdinal(body string, hiveIdx [][]int, fieldOff int) string {
	best := ""
	bestPos := -1
	for _, h := range hiveIdx {
		if h[0] < fieldOff && h[0] > bestPos {
			bestPos = h[0]
			best = body[h[2]:h[3]]
		}
	}
	return best
}

