func extractSqflite(src, path string) []types.EntityRecord {
	var out []types.EntityRecord
	for _, m := range sqfliteCreateRe.FindAllStringSubmatchIndex(src, -1) {
		table := src[m[2]:m[3]]
		colBody := src[m[4]:m[5]]
		line := lineAt(src, m[0])
		t := newDartSchema(table, "table", "sqflite", path, line,
			"INFERRED_FROM_SQFLITE_DDL")
		t.Properties["orm"] = "sqflite"
		t.Properties["raw_sql"] = "true"
		t.ID = t.ComputeID()
		out = append(out, t)
		for _, c := range parseSQLColumns(colBody) {
			col := newDartSchema(c.name, "column", "sqflite", path, line,
				"INFERRED_FROM_SQFLITE_DDL")
			col.Properties["sql_type"] = c.typ
			col.Properties["model"] = table
			if c.primary {
				col.Properties["primary_key"] = "true"
			}
			col.ID = col.ComputeID()
			out = append(out, col)
		}
	}
	return out
}

type sqlColumn struct {
	name    string
	typ     string
	primary bool
}

// parseSQLColumns splits a CREATE TABLE column body into column defs. Best-
// effort: skips table-level constraint clauses (PRIMARY KEY (...), FOREIGN
// KEY, UNIQUE(...)) that have no leading column name.
func parseSQLColumns(body string) []sqlColumn {
	var out []sqlColumn
	for _, raw := range splitTopLevel(body) {
		f := strings.Fields(strings.TrimSpace(raw))
		if len(f) < 1 {
			continue
		}
		head := strings.ToUpper(f[0])
		// Skip table-level constraint lines.
		if head == "PRIMARY" || head == "FOREIGN" || head == "UNIQUE" ||
			head == "CHECK" || head == "CONSTRAINT" {
			continue
		}
		name := strings.Trim(f[0], "\"'`")
		typ := ""
		if len(f) >= 2 {
			typ = strings.ToUpper(f[1])
		}
		primary := strings.Contains(strings.ToUpper(raw), "PRIMARY KEY")
		out = append(out, sqlColumn{name: name, typ: typ, primary: primary})
	}
	return out
}

// splitTopLevel splits on commas that are not nested inside parentheses.
func splitTopLevel(s string) []string {
	var out []string
	depth := 0
	start := 0
	for i, r := range s {
		switch r {
		case '(':
			depth++
		case ')':
			depth--
		case ',':
			if depth == 0 {
				out = append(out, s[start:i])
				start = i + 1
			}
		}
	}
	out = append(out, s[start:])
	return out
}

