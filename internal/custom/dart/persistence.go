// newDartSchema builds a SCOPE.Schema entity stamped with the given Dart
// persistence framework + provenance. Subtype grain: model/table/column/
// collection/database/dao/association.
func newDartSchema(name, subtype, framework, path string, line int, provenance string) types.EntityRecord {
	e := types.EntityRecord{
		Name:       name,
		Kind:       "SCOPE.Schema",
		Subtype:    subtype,
		SourceFile: path,
		Language:   "dart",
		StartLine:  line,
		EndLine:    line,
		Properties: map[string]string{
			"framework":  framework,
			"provenance": provenance,
		},
	}
	return e
}

func lineAt(src string, byteOff int) int {
	return strings.Count(src[:byteOff], "\n") + 1
}

// ---------------------------------------------------------------------------
// drift
// ---------------------------------------------------------------------------

func extractDrift(src, path string, cg codegenInfo) []types.EntityRecord {
	var out []types.EntityRecord
	for _, m := range driftTableRe.FindAllStringSubmatchIndex(src, -1) {
		name := src[m[2]:m[3]]
		line := lineAt(src, m[0])
		bodyEnd := findBraceEndByte(src, m[1]+indexBrace(src[m[1]:]))
		bodyStart := m[1]
		body := ""
		if bodyStart < bodyEnd && bodyEnd <= len(src) {
			body = src[bodyStart:bodyEnd]
		}

		table := newDartSchema(name, "table", "drift", path, line,
			"INFERRED_FROM_DRIFT_TABLE")
		stampCodegen(&table, cg)
		table.Properties["orm"] = "drift"
		table.ID = table.ComputeID()
		out = append(out, table)

		for _, c := range driftColumnRe.FindAllStringSubmatchIndex(body, -1) {
			colName := body[c[4]:c[5]]
			colType := body[c[2]:c[3]]
			builder := body[c[6]:c[7]]
			cline := line + strings.Count(body[:c[0]], "\n")
			col := newDartSchema(colName, "column", "drift", path, cline,
				"INFERRED_FROM_DRIFT_COLUMN")
			col.Properties["column_type"] = colType
			col.Properties["sql_type"] = driftSQLType(builder)
			col.Properties["model"] = name
			if strings.Contains(body[c[1]:minInt(c[1]+120, len(body))], "autoIncrement") {
				col.Properties["primary_key"] = "true"
			}
			col.ID = col.ComputeID()
			out = append(out, col)
		}
	}

	// @DriftDatabase(tables: [...]) → database node + REFERENCES edges.
	for _, m := range driftDatabaseRe.FindAllStringSubmatchIndex(src, -1) {
		list := src[m[2]:m[3]]
		line := lineAt(src, m[0])
		// The annotated class name is the first `class X` after the annotation.
		dbName := "DriftDatabase"
		tail := src[m[1]:]
		if cm := regexp.MustCompile(`class\s+([A-Z]\w*)`).FindStringSubmatch(tail); cm != nil {
			dbName = cm[1]
		}
		db := newDartSchema(dbName, "database", "drift", path, line,
			"INFERRED_FROM_DRIFT_DATABASE")
		db.Properties["orm"] = "drift"
		for _, t := range splitList(list) {
			db.Relationships = append(db.Relationships, types.RelationshipRecord{
				ToID: t,
				Kind: "REFERENCES",
				Properties: map[string]string{
					"to_model": t,
					"ref_kind": "drift_table",
				},
			})
		}
		db.ID = db.ComputeID()
		out = append(out, db)
	}
	return out
}

// driftSQLType maps a drift column builder root to its SQL type.
func driftSQLType(builder string) string {
	switch builder {
	case "integer", "int64":
		return "INTEGER"
	case "text":
		return "TEXT"
	case "boolean":
		return "BOOLEAN"
	case "real":
		return "REAL"
	case "dateTime":
		return "DATETIME"
	case "blob":
		return "BLOB"
	}
	return builder
}

